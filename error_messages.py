def get_binary_error():
    """–°–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ –¥–ª—è –¥–≤–æ–∏—á–Ω–æ–π –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏"""
    return format_error_with_example(
        "–í–≤–µ–¥–µ–Ω–Ω–∞—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å —Å–æ–¥–µ—Ä–∂–∏—Ç –Ω–µ–¥–æ–ø—É—Å—Ç–∏–º—ã–µ —Å–∏–º–≤–æ–ª—ã.\n"
        "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ —Ç–æ–ª—å–∫–æ —Å–∏–º–≤–æ–ª—ã 0 –∏ 1.",
        "1010",
        "–î–≤–æ–∏—á–Ω–∞—è –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç—å"
    )


def get_number_error(min_val=None, max_val=None):
    """–°–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ –¥–ª—è —á–∏—Å–ª–æ–≤–æ–≥–æ –∑–Ω–∞—á–µ–Ω–∏—è"""
    constraints = []
    if min_val is not None:
        constraints.append(f"–Ω–µ –º–µ–Ω—å—à–µ {min_val}")
    if max_val is not None:
        constraints.append(f"–Ω–µ –±–æ–ª—å—à–µ {max_val}")
    
    constraint_text = f" (–¥–æ–ª–∂–Ω–æ –±—ã—Ç—å {', '.join(constraints)})" if constraints else ""
    
    return format_error_with_example(
        f"–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ —á–∏—Å–ª–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ{constraint_text}.",
        "123" if min_val is None else str(min_val),
        "–ß–∏—Å–ª–æ"
    )


def get_digits_error(length=None):
    """–°–æ–æ–±—â–µ–Ω–∏–µ –æ–± –æ—à–∏–±–∫–µ –¥–ª—è —Å—Ç—Ä–æ–∫–∏ –∏–∑ —Ü–∏—Ñ—Ä"""
    length_text = f" (–¥–æ–ª–∂–Ω–æ –±—ã—Ç—å {length} —Ü–∏—Ñ—Ä)" if length else ""
    
    return format_error_with_example(
        f"–°—Ç—Ä–æ–∫–∞ –¥–æ–ª–∂–Ω–∞ —Å–æ–¥–µ—Ä–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ —Ü–∏—Ñ—Ä—ã{length_text}.",
        "123456789" if length is None else "1" * length,
        "–¶–∏—Ñ—Ä—ã"
    )


